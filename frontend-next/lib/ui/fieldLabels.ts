function normalizeEndpoint(endpoint?: string): string {
  const raw = String(endpoint || "")
  return raw.split("?")[0].split("#")[0]
}

export function bloodbankResourceKeyFromEndpoint(endpoint?: string): string | null {
  const e = normalizeEndpoint(endpoint)
  // Matches both collection and detail endpoints:
  // - /bloodbank/storage/
  // - /bloodbank/storage/123/
  const m = e.match(/\/bloodbank\/([^/]+)(?:\/|$)/)
  return m?.[1] || null
}

export function warehouseResourceKeyFromEndpoint(endpoint?: string): string | null {
  const e = normalizeEndpoint(endpoint)
  const m = e.match(/\/warehouse\/([^/]+)(?:\/|$)/)
  return m?.[1] || null
}

export function isInternalField(name: string): boolean {
  return INTERNAL_FIELDS.has(String(name || ""))
}

function humanizeSnakeCase(name: string): string {
  const s = String(name || "").trim()
  if (!s) return ""
  return translateFieldName(s) || titleCaseLabel(s.replace(/_/g, " "))
}

function normalizeFieldKey(value: string): string {
  return String(value || "")
    .trim()
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/[^A-Za-z0-9À-ÿ]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .toLowerCase()
}

function titleCaseLabel(value: string): string {
  const text = String(value || "").trim().replace(/\s+/g, " ")
  if (!text) return ""
  return text.charAt(0).toLocaleUpperCase("pt") + text.slice(1)
}

function lowerContext(value: string): string {
  const text = String(value || "").trim()
  if (!text) return ""
  return text.charAt(0).toLocaleLowerCase("pt") + text.slice(1)
}

function tokenLabel(token: string): string {
  const key = normalizeFieldKey(token)
  if (!key) return ""
  if (ACRONYM_LABELS[key]) return ACRONYM_LABELS[key]
  if (TOKEN_LABELS[key]) return TOKEN_LABELS[key]
  return key
}

function plainTranslatedLabel(key: string): string {
  const normalized = normalizeFieldKey(key)
  if (!normalized) return ""
  if (COMMON_LABELS[normalized]) return COMMON_LABELS[normalized]
  const tokens = normalized.split("_").filter(Boolean)
  return titleCaseLabel(tokens.map(tokenLabel).filter(Boolean).join(" "))
}

function entityContextLabel(key: string): string {
  const normalized = normalizeFieldKey(key)
  if (!normalized) return ""
  if (ENTITY_CONTEXT_LABELS[normalized]) return ENTITY_CONTEXT_LABELS[normalized]
  const plain = plainTranslatedLabel(normalized)
  return plain ? `de ${lowerContext(plain)}` : ""
}

