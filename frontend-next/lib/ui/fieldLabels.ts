function translateFieldName(rawName: string): string {
  const name = normalizeFieldKey(rawName)
  if (!name) return ""

  if (COMMON_LABELS[name]) return COMMON_LABELS[name]

  const relationLabelMatch = name.match(/^(.+)__(name|code|label|status)$/)
  if (relationLabelMatch) {
    const [, base, suffix] = relationLabelMatch
    const suffixLabel = DESCRIPTIVE_SUFFIX_LABELS[suffix] || plainTranslatedLabel(suffix)
    return titleCaseLabel(`${suffixLabel} ${entityContextLabel(base)}`)
  }

  if (name.endsWith("_id")) {
    return translateFieldName(name.slice(0, -3))
  }

  if (name.endsWith("_at")) {
    return translateTimestampField(name, "at")
  }

  if (name.endsWith("_on")) {
    return translateTimestampField(name, "on")
  }

  if (name.endsWith("_by")) {
    const base = name.slice(0, -3)
    return `${plainTranslatedLabel(base)} por`
  }

  const tokens = name.split("_").filter(Boolean)
  const lastToken = tokens[tokens.length - 1]
  if (tokens.length > 1 && UNIT_SUFFIX_LABELS[lastToken]) {
    const base = tokens.slice(0, -1).join("_")
    return `${plainTranslatedLabel(base)} (${UNIT_SUFFIX_LABELS[lastToken]})`
  }

  if (tokens.length > 1 && DESCRIPTIVE_SUFFIX_LABELS[lastToken]) {
    const base = tokens.slice(0, -1).join("_")
    return titleCaseLabel(`${DESCRIPTIVE_SUFFIX_LABELS[lastToken]} ${entityContextLabel(base)}`)
  }

  if (tokens.length > 1 && tokens[0] === "is") {
    return `${plainTranslatedLabel(tokens.slice(1).join("_"))}?`
  }

  if (tokens.length > 1 && tokens[0] === "has") {
    return `Tem ${lowerContext(plainTranslatedLabel(tokens.slice(1).join("_")))}?`
  }

  return plainTranslatedLabel(name)
}

function translateTitle(title: string): string {
  const clean = String(title || "").trim()
  if (!clean) return ""
  if (/[áàãâéêíóôõúç]/i.test(clean)) return clean
  const key = normalizeFieldKey(clean)
  if (!key) return clean
  return translateFieldName(key)
}

export function fieldLabel(opts: {
  endpoint?: string
  name: string
  title?: string
}): string {
  const name = String(opts.name || "")
  const title = typeof opts.title === "string" ? opts.title.trim() : ""
  const normalizedName = normalizeFieldKey(name)

  const resourceKey = bloodbankResourceKeyFromEndpoint(opts.endpoint)
  if (resourceKey && BLOODBANK_LABELS[resourceKey]?.[name]) {
    return BLOODBANK_LABELS[resourceKey][name]
  }

  const warehouseKey = warehouseResourceKeyFromEndpoint(opts.endpoint)
  if (warehouseKey) {
    const specific = WAREHOUSE_LABELS[warehouseKey]?.[name]
    if (specific) return specific
    if (WAREHOUSE_COMMON_LABELS[name]) return WAREHOUSE_COMMON_LABELS[name]
  }

  const educationKey = educationResourceKeyFromEndpoint(opts.endpoint)
  if (educationKey && EDUCATION_LABELS[educationKey]?.[name]) {
    return EDUCATION_LABELS[educationKey][name]
  }

  const translatedTitle = translateTitle(title)
  if (translatedTitle && GENERIC_FIELD_NAMES.has(normalizedName)) {
    return translatedTitle
  }

  if (COMMON_LABELS[normalizedName]) return COMMON_LABELS[normalizedName]

  const translatedName = translateFieldName(name)
  if (translatedName) return translatedName

  if (translatedTitle && title.toLowerCase() !== name.toLowerCase()) return translatedTitle

  return humanizeSnakeCase(name)
}
